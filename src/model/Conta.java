
package model;


public class Conta {
    private String Serv;
    private String Preco;
    private String Cod;

    public String getServ() {
        return Serv;
    }

    public void setServ(String Serv) {
        this.Serv = Serv;
    }

    public String getPreco() {
        return Preco;
    }

    public void setPreco(String Preco) {
        this.Preco = Preco;
    }

    public String getCod() {
        return Cod;
    }

    public void setCod(String Cod) {
        this.Cod = Cod;
    }
    
    
}
